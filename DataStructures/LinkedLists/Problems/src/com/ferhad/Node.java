package com.ferhad;

/**
 * This class is just for describing Nodes of LinkedList in the Problems.
 */
public class Node {
    private Node next;
    private int data;

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

}
